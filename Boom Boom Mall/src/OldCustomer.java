    private void btncustomerinformationActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btncustomerinformationActionPerformed
        String username=tfusername.getText();
        String password=pfpassword.getText();


        String query = "SELECT * FROM customer WHERE Username = '" + (username) + "' && password='"+(password)+"'" ;
        try {
          
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/boomboom","root","12345");
          
            Statement stmt = con.createStatement();
            ResultSet rs = stmt.executeQuery(query);

            if (rs.next()) {
          
                String name = rs.getString("name");
                String card=rs.getString("card");
                double balance=rs.getDouble("balance");
               
                      
                tfname.setText(""+name);
                tfcard.setText(""+card);
                tfbalance.setText(""+balance);
              
               
             
                con.close();
                stmt.close();
                rs.close();
                panelinformation.setVisible(true);
            }
            else 
            {
                JOptionPane.showMessageDialog(null, "Invalid Username or Password");
                tfusername.setText("");
                pfpassword.setText("");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.getMessage());
        }   
    }//GEN-LAST:event_btncustomerinformationActionPerformed

