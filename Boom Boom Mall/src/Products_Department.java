    private void btnsearchbookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchbookActionPerformed
        DefaultTableModel model=(DefaultTableModel)tblbooksearch.getModel();
        int rows=model.getRowCount();
        if(rows>2)
        {
            for(int i=2;i<rows;i++)
            {
                model.removeRow(2);
            }
        }
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con =  DriverManager.getConnection("jdbc:mysql://localhost:3306/boomboom","root","12345");
            ResultSet rs = null;
            Statement stmt = null;
            String query;
            String booksearch=tfsearch.getText();
            switch(combosearchcriteria.getSelectedIndex())
            {
                case 0:
                query = "SELECT * FROM book where Book_Title='"+(booksearch)+"'";
                stmt = con.createStatement();
                rs = stmt.executeQuery(query);
                while(rs.next())
                {
                    model.addRow(new Object[]
                        {
                            rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getDouble(5)});
                }
                System.out.println(rs.getString(1));

                break;
                case 1:

                query = "SELECT * FROM book where Author='"+(booksearch)+"'";
                stmt = con.createStatement();
                rs = stmt.executeQuery(query);
                while(rs.next())
                {
                    model.addRow(new Object[]
                        {
                            rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getDouble(5)});
                }

                break;
                case 2:
                String booktype=(String) combobooktype.getSelectedItem();

                query = "SELECT * FROM book where Book_Type='"+(booktype)+"'";
                stmt = con.createStatement();
                rs = stmt.executeQuery(query);
                while(rs.next())
                {
                    model.addRow(new Object[]
                        {
                            rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getDouble(5)});
                }

                break;
                case 3:
                double minprice=Double.parseDouble(tfminprice.getText());
                double maxprice=Double.parseDouble(tfmaxprice.getText());
                query = "SELECT * FROM book where Price between'"+(minprice)+"'and'"+(maxprice)+"'";
                stmt = con.createStatement();
                rs = stmt.executeQuery(query);
                while(rs.next())
                {
                    model.addRow(new Object[]
                        {
                            rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getDouble(5)});
                }

                break;

            }
            rs.close();
            stmt.close();
            con.close();
        }

        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }

    }//GEN-LAST:event_btnsearchbookActionPerformed

