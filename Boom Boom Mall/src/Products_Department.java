    private void btnsearchmovieActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnsearchmovieActionPerformed
        DefaultTableModel model=(DefaultTableModel)tblmoviesearch.getModel();
        int rows=model.getRowCount();
        if(rows>2)
        {
            for(int i=2;i<rows;i++)
            {
                model.removeRow(2);
            }
        }
        try
        {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con =  DriverManager.getConnection("jdbc:mysql://localhost:3306/boomboom","root","12345");
            ResultSet rs = null;
            Statement stmt = con.createStatement();
            String query;
            String moviesearch=tfsearch.getText();
            switch(combosearchcriteria.getSelectedIndex())
            {
                case 0:
                query = "SELECT * FROM movie where Movie_Title='"+(moviesearch)+"'";
               
                rs = stmt.executeQuery(query);
                while(rs.next())
                {
                    model.addRow(new Object[]
                        {
                            rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)});
                }
              
                break;
                case 1:

                query = "SELECT * FROM movie where LeadRole='"+(moviesearch)+"'";
               
                rs = stmt.executeQuery(query);
                while(rs.next())
                {
                    model.addRow(new Object[]
                        {
                            rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)});
                }

                break;
                case 2:
                String movietype=(String) combomovietype.getSelectedItem();

                query = "SELECT * FROM movie where movie_Type='"+(movietype)+"'";
              
                rs = stmt.executeQuery(query);
                while(rs.next())
                {
                    model.addRow(new Object[]
                        {
                            rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4)});
                }

                break;

            }
         
        }

        catch(Exception e)
        {
            JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_btnsearchmovieActionPerformed

