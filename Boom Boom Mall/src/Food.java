    private void btnbillActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbillActionPerformed
        double dessert,veg,nonveg,payment,bill;
        int discount;
        dessert = Double.parseDouble(tfdessert.getText());
        veg = Double.parseDouble(tfveg.getText());
        nonveg = Double.parseDouble(tfnonveg.getText());
        payment = dessert+veg+nonveg;
        tftotalpayment.setText(""+payment);
        if(payment>=1000)
        {
           discount=(int) (payment*0.02);
        }
       
        else if(payment>=2000)
        {
            discount=(int) (payment*0.05);
        }
        else if(payment>=5000)
        {
            discount=(int) (payment*0.1);
        }
        else if(payment>=10000)
        {
            discount=(int) (payment*0.15);
        }
        else
        {
            discount=0;
        }
        tfdiscount.setText(""+discount);
        bill=payment-discount;
        tfnetbill.setText(""+bill);
    }//GEN-LAST:event_btnbillActionPerformed

